
package org.paris.batch.datafile;

/**
 * @author santusbr
 * Type de format pour un datafile
 */
public class DataFileType {
	
	/**
	 * Type de format : Format d'entrée d'un fichier
	 */
	public static final String FORMAT_IN = "in";
	/**
	 * Type de format : Format de sortie d'un fichier
	 */
	public static final String FORMAT_OUT = "out";
	/**
	 * Origine d'un fichier : Origine du répertoire source
	 */
	public static final String ORIGINE_SOURCE = "source";
	/**
	 * Origine d'un fichier : Origine du répertoire destination
	 */
	public static final String ORIGINE_DESTINATION = "destination";
	/**
	 * Type d'un fichier : Fichier CSV
	 */
	public static final String TYPE_CSV = "csv";
	/**
	 * Type d'un fichier : Fichier colonné
	 */
	public static final String TYPE_COLONNE = "colonne";
}
